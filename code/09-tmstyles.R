library(spData)
library(tmap)
legend_title = expression("Area (km"^2*")")
map_nza = tm_shape(nz) +
  tm_fill(col = "Land_area", title = legend_title) + tm_borders()
s1 = map_nza + tm_style_bw() 
# + tm_layout("tm_style_bw")
s2 = map_nza + tm_style_classic()
# + tm_layout("tm_style_classic")
s3 = map_nza + tm_style_cobalt()
# + tm_layout("tm_style_cobalt")
s4 = map_nza + tm_style_col_blind()
# + tm_layout("tm_style_col_blind")
tmap_arrange(s1, s2, s3, s4, nrow = 1)
